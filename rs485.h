#ifndef __RS485_H__
#define __RS485_H__

#include <STC32G.H>

/*=============================================================================
 * Modbus RTU 从机配置
 *===========================================================================*/
#define MODBUS_SLAVE_ADDR       0x01        // 本机从站地址（可改）
#define MODBUS_REG_NUM          16          // 保持寄存器数量（地址 0x0000~0x000F）
#define MODBUS_COIL_NUM         8           // 线圈数量（地址 0x0000~0x0007）

/*-----------------------------------------------------------------------------
 * RS485 方向控制（与 uart.c 中 P1.2 一致）
 *---------------------------------------------------------------------------*/
#define RS485_TX()  // 自动流控，无需操作
#define RS485_RX()  // 自动流控，无需操作

/*-----------------------------------------------------------------------------
 * Modbus RTU 帧缓冲区
 *---------------------------------------------------------------------------*/
#define MODBUS_BUF_SIZE     64          // 最大帧长度

/*-----------------------------------------------------------------------------
 * 3.5字符超时（9600bps，1字符≈1.04ms → 3.5字符≈4ms → 取5个1ms tick）
 * 若更换波特率请重新计算：T = 3.5 × 11 / baudrate × 1000 (ms)
 *---------------------------------------------------------------------------*/
#define MODBUS_TIMEOUT_MS   5

/*-----------------------------------------------------------------------------
 * Modbus 功能码
 *---------------------------------------------------------------------------*/
#define FC_READ_COILS           0x01
#define FC_READ_HOLDING_REGS    0x03
#define FC_WRITE_SINGLE_COIL    0x05
#define FC_WRITE_SINGLE_REG     0x06
#define FC_WRITE_MULTI_REGS     0x10

/*-----------------------------------------------------------------------------
 * Modbus 异常码
 *---------------------------------------------------------------------------*/
#define EX_ILLEGAL_FUNCTION     0x01
#define EX_ILLEGAL_DATA_ADDR    0x02
#define EX_ILLEGAL_DATA_VALUE   0x03

/*=============================================================================
 * 对外接口
 *===========================================================================*/

/* 保持寄存器（主程序可直接读写） */
extern unsigned int  modbus_regs[MODBUS_REG_NUM];

/* 线圈（按位存储，bit0=线圈0，依此类推） */
extern unsigned char modbus_coils;

/*
 * 初始化 Modbus（需在 UART2_Init 之后调用）
 * 主要完成方向脚初始状态配置
 */
void Modbus_Init(void);

/*
 * 放在 Timer0_ISR 中，每 1ms 调用一次
 * 用于 3.5 字符超时检测
 */
void Modbus_TimerTick(void);

/*
 * 放在 UART2_ISR 接收分支中，每收到1字节调用一次
 * 参数：刚收到的字节
 */
void Modbus_RxByte(unsigned char dat);

/*
 * 放在主循环 while(1) 中轮询
 * 当接收完整帧后处理并回复
 */
void Modbus_Poll(void);

#endif /* __MODBUS_H__ */
