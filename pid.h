#ifndef __PID_H__
#define __PID_H__

/*==============================================================================
 * pid.h  —  增量式PID控制器
 *
 * 结构：
 *   速度环（Speed PID）：输入=目标rpm vs 实际rpm，输出=PWM增量
 *   位置环（Angle PID）：输入=目标0.1° vs 实际0.1°，输出=目标速度（rpm）
 *   串级控制：位置环输出 → 速度环输入
 *
 * 增量式PID公式：
 *   delta_u = Kp*(e[k]-e[k-1]) + Ki*e[k] + Kd*(e[k]-2*e[k-1]+e[k-2])
 *   u[k] = u[k-1] + delta_u
 *==============================================================================*/

/*=============================================================================
 * PID 参数结构体
 *===========================================================================*/
typedef struct
{
    /* 参数 */
    int Kp;             /* 比例系数（放大100倍存储，避免浮点） */
    int Ki;             /* 积分系数（放大100倍存储）           */
    int Kd;             /* 微分系数（放大100倍存储）           */

    /* 状态 */
    int e0;             /* 当前误差  e[k]   */
    int e1;             /* 上次误差  e[k-1] */
    int e2;             /* 上上次误差e[k-2] */
    long output;        /* 累计输出值（u[k]）*/

    /* 限幅 */
    long out_max;       /* 输出上限 */
    long out_min;       /* 输出下限 */
    int  dead_band;     /* 死区（误差绝对值小于此值时不动作）  */
} PID_t;

/*=============================================================================
 * 函数声明
 *===========================================================================*/
void PID_Init(PID_t *pid,
              int kp, int ki, int kd,
              long out_max, long out_min,
              int dead_band);

void PID_Reset(PID_t *pid);

long PID_Calc(PID_t *pid, int setpoint, int feedback);

#endif /* __PID_H__ */
