#ifndef __PID_H__
#define __PID_H__

/*==============================================================================
 * pid.h  —  位置式PID控制器
 *
 * 结构：
 *   速度环（Speed PID）：输入=目标rpm vs 实际rpm，输出=PWM占空比偏移量
 *   位置环（Angle PID）：输入=目标0.1° vs 实际0.1°，输出=目标速度（rpm）
 *   串级控制：位置环输出 → 速度环输入
 *
 * 位置式PID公式：
 *   u[k] = Kp*e[k] + Ki*sum(e) + Kd*(e[k]-e[k-1])
 *   Kp/Ki/Kd 放大100倍整数存储，计算时再÷100，避免浮点运算
 *==============================================================================*/

/*=============================================================================
 * PID 参数结构体
 *===========================================================================*/
typedef struct
{
    /* 参数 */
    int  Kp;            /* 比例系数（放大100倍存储，避免浮点）            */
    int  Ki;            /* 积分系数（放大ki_scale倍存储）                 */
    int  Kd;            /* 微分系数（放大100倍存储）                      */
    int  ki_scale;      /* Ki缩放因子：默认100，角度环设10000（精度0.0001）*/

    /* 状态 */
    int  e_prev;        /* 上次误差 e[k-1]（用于微分项）       */
    long integral;      /* 积分累加器 sum(e)                   */
    long output;        /* 当前输出值                          */

    /* 限幅 */
    long out_max;       /* 输出上限 */
    long out_min;       /* 输出下限 */
    int  dead_band;     /* 死区（误差绝对值小于此值时输出为0） */
} PID_t;

/*=============================================================================
 * 函数声明
 *===========================================================================*/
void PID_Init(PID_t *pid,
              int kp, int ki, int kd,
              long out_max, long out_min,
              int dead_band);

void PID_Reset(PID_t *pid);

long PID_Calc(PID_t *pid, int setpoint, int feedback);

#endif /* __PID_H__ */
